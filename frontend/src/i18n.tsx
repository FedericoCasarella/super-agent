type Ctx = { lang: Lang; setLang: (l: Lang) => Promise<void>; t: (key: string) => string };
const I18nContext = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('it');
  useEffect(() => {
    api.settings().then((s) => { if (s?.language === 'it' || s?.language === 'en') setLangState(s.language); }).catch(() => {});
  }, []);
  const setLang = useCallback(async (l: Lang) => {
    setLangState(l);
    try { await fetch('/api/settings/language', { method: 'PUT', headers: { 'content-type': 'application/json' }, body: JSON.stringify({ language: l }) }); } catch {}
  }, []);
  const t = useCallback((key: string) => DICT[lang][key] ?? DICT.en[key] ?? key, [lang]);
  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n outside provider');
  return ctx;
}
